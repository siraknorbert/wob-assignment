package hu.wob.assignment.constant;

/**
 * Constants for shell terminal color codes.
 */
public interface IShellTerminalColorCodeConstants {

    /**
     * Green color
     */
    String GREEN = "\u001B[32m";

    /**
     * Red color
     */
    String RED = "\u001B[31m";

    /**
     * Reset color
     */
    String RESET = "\u001B[0m";
}
